//Write a program to demonstrate increment & decrement operators.
public class IncrementandDecrement {

    public static void main(String[] args) {
        int a = 10;

        // Demonstrating increment operator
        System.out.println("Initial value of a: " + a);
        System.out.println("Value of a after increment: " + (++a));

        // Demonstrating decrement operator
        System.out.println("Value of a after decrement: " + (--a));
    }
}
